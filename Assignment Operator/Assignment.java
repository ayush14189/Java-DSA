import java.util.*;

public class Assignment{
    public static void main(String args[]){
        int A = 10;
        System.out.println("A = " + A);
        A += 5;
        System.out.println("+= 5: " + A);
        int B = 10;
        B -= 5;
        System.out.println("-= 5: " + B);
        int C = 10;
        C *= 5;
        System.out.println("*= 5: " + C);
        int D = 10;
        D /= 5;
        System.out.println("/= 5: " + D);
    }
}