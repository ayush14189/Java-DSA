public class DataTypes{
    public static void main(String args[]){
        // byte - stores byte values upto 256
        byte b = 8;

        //char - stores single characters such as a,b,c..
        char ch = 'a';
        
        //boolean - stores either true or false 
        boolean var = true;
        
        //float - stores decimal values
        float price = 1.5;
        
        //int - stores integer numbers
        int number = 25;
        
        //long - stores large integer numbers
        //double - stores large decimal values

        //short - stores small numbers
        short n = 240;
    }
}